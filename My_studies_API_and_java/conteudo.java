public class conteudo {
    
    private final String title;
    private final String url_Image;

    public conteudo(String title, String url_Image) {
        this.title = title;
        this.url_Image = url_Image;
    }
    
    public String getTitle() {
        return title;
    }
    
    public String getUrl_Image() {
        return url_Image;
    }

    
}
